function ComponentNodeData(name, 
                           componentClass, 
                           nodeId,
                           source,
                           destination,
                           ownerId)
{
    this.Name = name;    
    this.ComponentClass = componentClass;
    this.NodeId = nodeId;
    this.Source =source;
    this.Destination =destination;
    this.OwnerId =ownerId;
}